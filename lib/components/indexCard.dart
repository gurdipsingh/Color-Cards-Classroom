class IndexCard {
  String name;
  int id;
  String information_One;
  String information_Two;
  String information_Three;

  IndexCard(String name, int id, String information_One, String information_Two,
      String information_Three) {
    this.name = name;
    this.id = id;
    this.information_One = information_One;
    this.information_Two = information_Two;
    this.information_Three = information_Three;
  }
}
